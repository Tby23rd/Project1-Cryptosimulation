    A cryptocurrency is a digital or virtual currency that is secured by cryptography, 
    which makes it nearly impossible to counterfeit or double-spend. Many cryptocurrencies are decentralized networks based on 
    blockchain technology—a distributed ledger enforced by a disparate network of computers.
    A cryptocurrency’s blockchain is the master public ledger that records and stores all prior transactions and activity, 
    validating ownership of all units of the currency at any given point in time.
</p>
<p>
    The idea for cryptocurrency first began in the late 1980’s, the idea was for a currency that could be sent
     untraceably and in a manner that did not require centralized entities (i.e. Banks). In 1995, 
     American cryptographer David Chaum implemented an anonymous cryptographic electronic money
      called Digicash. It was an early form of cryptographic electronic payments which required
       user software to withdraw from a bank and required specific encrypted keys before it could 
       be sent to a recipient. Bit Gold, often called a direct precursor to Bitcoin, was designed
        in 1998 by Nick Szabo. It required a participant to dedicate computer power to solving cryptographic puzzles, and those
     who solved the puzzle received the reward. If you put Chaum’s and Szabo’s concepts together, then you have something that resembles Bitcoin.
</p>
